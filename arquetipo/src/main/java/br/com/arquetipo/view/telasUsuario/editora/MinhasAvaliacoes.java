    private void formWindowOpened() {

        try {

            lista = new AvaliacaoDAO().BuscarPeloIdEditora();
            preencheTabela(lista);

        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar " + ex.getMessage());
        }

    }

    private void preencheTabela(ArrayList<Avaliacao> lista) throws SQLException, ClassNotFoundException {
        //System.out.println(lista.get(0).getGenero() + " " + lista.get(0).getTitulo() + " AQUII");
        DefaultTableModel dftc = (DefaultTableModel) jTavaliacoes.getModel();

        while (dftc.getRowCount() > 0) {
            dftc.removeRow(0);
        }

        for (Avaliacao ava : lista) {

            String[] linha = {ava.getId_avaliacao() + "", ava.getHistoria().getEscritor().getNome(), ava.getHistoria().getTitulo(), "" + ava.getNota()};

            dftc.addRow(linha);
        }

    }
