    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int linhaSelecionada = jTavaliacoes.getSelectedRow();

        if (linhaSelecionada > -1) {

            int id = Integer.parseInt(jTavaliacoes.getValueAt(linhaSelecionada, 0).toString());
            String nome = jTavaliacoes.getValueAt(linhaSelecionada, 1).toString();
            String titulo = jTavaliacoes.getValueAt(linhaSelecionada, 2).toString();
            int nota = Integer.parseInt(jTavaliacoes.getValueAt(linhaSelecionada, 3).toString());
            NovaAvaliacao view = new NovaAvaliacao(this, true);
            view.preencheRevaliacao(id, nome, titulo, nota);
            view.setLocationRelativeTo(null);
            
            this.dispose();
            view.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha", "Aviso", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int linhaSelecionada = jTavaliacoes.getSelectedRow();

        if (linhaSelecionada > -1) {

            int id = Integer.parseInt(jTavaliacoes.getValueAt(linhaSelecionada, 0).toString());
            Avaliacao ava = new Avaliacao();
            ava.setId_avaliacao(id);
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja deletar?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    new AvaliacaoDAO().apagar(ava);
                    formWindowOpened();
                } catch (SQLException ex) {
                    Logger.getLogger(MinhasAvaliacoes.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(MinhasAvaliacoes.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

