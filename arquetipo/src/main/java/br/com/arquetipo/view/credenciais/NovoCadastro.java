    private void JbCadastrarActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_JbCadastrarActionPerformed
        // TODO add your handling code here:
        
        //pega os atributos da view
        String nome = JtNomeCompleto.getText();
        String tipoUsuario = (String)JcTipoUsuario.getSelectedItem();
        String nomeDeUsuario = JtNomeUsuario.getText();
        char [] senha = JpSenha.getPassword();
        
        obj.setNome(nome);
        obj.setTipoUsuario(tipoUsuario);
        //Converte para String
        String senhaString = new String(senha);


        UsuarioDAO usuDao = new UsuarioDAO();
        IGenericDAO gDAO = null;
        Usuario usu = null;
        switch (tipoUsuario) {
            
            case "Editora":
                usu = new Editora();                
                gDAO = new EditoraDAO();
                break;
            case "Escritor":
                usu = new Escritor();
                gDAO= new EscritorDAO(); 
                break;
            default:
                usu = new Leitor();

                gDAO = new LeitorDAO();
                break;
        }
        
        
       
        
        usu.setLogin(nomeDeUsuario);
        usu.setSenha(senhaString);
        usu.setNome(nome);
        usuDao.inserir(usu);
        gDAO.inserir(usu);
        
        InicialUsuario inicialUsuario = new InicialUsuario();
        inicialUsuario.setLocationRelativeTo(null);
        inicialUsuario.setVisible(true);
        inicialUsuario.identificaUsuario();
        this.dispose();
        
        
    }//GEN-LAST:event_JbCadastrarActionPerformed

